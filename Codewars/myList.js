// รายการเริ่มต้นเป็นรายการว่าง
let myList = [];

// รับข้อมูลจากผู้ใช้และตัดคำเป็นตัวเลขหรือข้อความ
let newItem = prompt("ป้อนรายการใหม่:");

// ถามผู้ใช้ว่าต้องการเพิ่มไว้ที่ด้านหน้าหรือด้านหลัง
let position = prompt("ต้องการเพิ่มไว้ที่ด้านหน้าหรือด้านหลัง (หน้า/หลัง):");

// เช็คคำตอบและใช้เมธอด unshift() หรือ push() เพิ่มข้อมูล
if (position === "หน้า") {
  myList.unshift(newItem);
} else if (position === "หลัง") {
  myList.push(newItem);
}

// แสดงรายการใหม่ทั้งหมด
console.log("รายการใหม่:");
console.log(myList);
